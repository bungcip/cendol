    #[test]
    fn test_de_splice_utf8() {
        // Simple case: no backslash
        assert_eq!(de_splice("🦀"), "🦀");

        // Simple splice
        assert_eq!(de_splice("a\\\nb"), "ab");

        // Splice with multi-byte UTF-8
        assert_eq!(de_splice("🦀\\\n🐙"), "🦀🐙");

        // Splice with whitespace and Windows line endings
        assert_eq!(de_splice("abc\\  \r\ndef"), "abcdef");

        // Regular backslash (not a splice)
        assert_eq!(de_splice("a\\b"), "a\\b");

        // Mixed
        assert_eq!(de_splice("🦀\\\\\n🐙"), "🦀\\🐙");
    }

    #[test]
    fn test_fast_skip_to_directive() {
        let source = b"
#if 0
  this is skipped;
  int x = #not_a_directive;
  // #fake directive in comment
  /* #another fake
     multiline */
  extern int y;
#else
  this is not skipped
#endif
";
        let sid = SourceId::new(2);
        let mut lexer = PPLexer::new(sid, Arc::from(&source[..]));

        // 1. Initial #if 0
        let t1 = lexer.next_token().unwrap();
        assert_eq!(t1.kind, PPTokenKind::Hash);
        let t2 = lexer.next_token().unwrap();
        assert_eq!(lexer.get_token_text(&t2), "if");
        let t3 = lexer.next_token().unwrap();
        assert_eq!(lexer.get_token_text(&t3), "0");
        let t4 = lexer.next_token().unwrap();
        assert_eq!(t4.kind, PPTokenKind::Eod);

        // 2. Fast skip!
        lexer.fast_skip_to_directive();

        // 3. Should be at #else
        let t5 = lexer.next_token().unwrap();
        assert_eq!(t5.kind, PPTokenKind::Hash);
        let t6 = lexer.next_token().unwrap();
        assert_eq!(lexer.get_token_text(&t6), "else");
    }
